# 리스트 언선

list_a = [1, 2, 3]
list_b = [4, 5, 6]
print('list_a : ', list_a)
print('list_b : ', list_b)

# 리스트 연산자 +, -, *
print('--------------------------')
list_c = list_a + list_b
list_d = list_a * 3
print('list_a+list_b = ', list_c)
print('list_a * 3 = ', list_d)

# 리스트 함수 len()
print('--------size 구하기--------')
print('len(list_a) : ', len(list_a))
print('len(list_b) : ', len(list_b))
print('len(list_c) : ', len(list_c))
print('len(list_d) : ', len(list_d))
